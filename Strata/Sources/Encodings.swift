import Foundation

// MARK: - OffsetBase

/// The numeric base used for displaying byte offsets.
public enum OffsetBase: String, CaseIterable {
    /// Hexadecimal (base 16).
    case hex = "Hex"
    /// Decimal (base 10).
    case decimal = "Decimal"
    /// Octal (base 8).
    case octal = "Octal"

    /// Formats an offset value as a string in this base.
    ///
    /// - Parameter offset: The byte offset.
    /// - Returns: A formatted string.
    public func format(_ offset: Int) -> String {
        switch self {
        case .hex:
            return String(format: "%08X", offset)
        case .decimal:
            return String(format: "%010d", offset)
        case .octal:
            return String(format: "%011o", offset)
        }
    }

    /// The header label for the offset column.
    public var headerLabel: String {
        switch self {
        case .hex: return "Offset(h)"
        case .decimal: return "Offset(d)"
        case .octal: return "Offset(o)"
        }
    }
}

// MARK: - TextEncoding

/// Character encodings for the text pane.
public enum TextEncoding: String, CaseIterable {
    /// Standard ASCII / Latin-1 (ISO 8859-1).
    case ascii = "ANSI"
    /// IBM PC CP437 (DOS).
    case cp437 = "DOS"
    /// EBCDIC (IBM mainframe).
    case ebcdic = "EBCDIC"
    /// Mac OS Roman.
    case macRoman = "Macintosh"

    /// Decodes a single byte to a displayable character.
    ///
    /// - Parameter byte: The raw byte value.
    /// - Returns: A tuple of the display character and whether it is printable.
    public func decode(_ byte: UInt8) -> (character: String, printable: Bool) {
        switch self {
        case .ascii:
            let printable = (0x20...0x7E).contains(byte)
            return (
                printable ? String(UnicodeScalar(byte)) : ".",
                printable
            )
        case .cp437:
            return decodeCp437(byte)
        case .ebcdic:
            return decodeEbcdic(byte)
        case .macRoman:
            return decodeMacRoman(byte)
        }
    }

    // MARK: - Private

    private func decodeCp437(_ byte: UInt8) -> (String, Bool) {
        if byte == 0x00 { return (".", false) }
        if byte == 0x20 { return (" ", true) }
        if let scalar = Self.cp437Table[byte] {
            return (String(scalar), true)
        }
        if (0x20...0x7E).contains(byte) {
            return (String(UnicodeScalar(byte)), true)
        }
        return (".", false)
    }

    private func decodeEbcdic(_ byte: UInt8) -> (String, Bool) {
        if let scalar = Self.ebcdicTable[byte] {
            return (String(scalar), true)
        }
        return (".", false)
    }

    private func decodeMacRoman(_ byte: UInt8) -> (String, Bool) {
        if (0x20...0x7E).contains(byte) {
            return (String(UnicodeScalar(byte)), true)
        }
        if byte >= 0x80 {
            if let scalar = Self.macRomanHighTable[byte] {
                return (String(scalar), true)
            }
        }
        return (".", false)
    }

